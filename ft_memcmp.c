#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			offset;
	unsigned char	*s1_c;
	unsigned char	*s2_c;

	s1_c = (unsigned char *)s1;
	s2_c = (unsigned char *)s2;
	offset = 0;
	while (offset < n - 1 && s1_c[offset] == s2_c[offset])
		offset++;
	return (s1_c[offset] - s2_c[offset]);
}
